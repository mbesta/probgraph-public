PROBLEMS=(tc)
KRON_SIZE=16
KRON_EDGES=128
OPTIONS="-g ${KRON_SIZE} -k ${KRON_EDGES} -y 0.1"

for PROB in ${PROBLEMS[@]}; do	
	echo "\n*****************************************************************"
	echo "*************RUNNING ${PROB} with BASELINE **********************"
	echo "*****************************************************************"
	./src/${PROB}_base ${OPTIONS}
	
	echo "\n*****************************************************************"
	echo "*************RUNNING ${PROB} with BASELINE **********************"
	echo "*****************************************************************"
	./src/${PROB}_1h ${OPTIONS} -t 0.01
	
	echo "\n*****************************************************************"
	echo "*************RUNNING ${PROB} with BASELINE **********************"
	echo "*****************************************************************"
	./src/${PROB}_bf ${OPTIONS} -t 0.5
done
